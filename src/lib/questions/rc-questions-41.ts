const passage1 = `The decipherment of Linear B in 1952 by British architect Michael Ventris represents one of the most significant achievements in twentieth-century archaeology. Linear B, a script found on clay tablets at Mycenaean palace sites in Greece and Crete, had puzzled scholars since its discovery by Sir Arthur Evans at Knossos in 1900. Evans believed the script recorded a pre-Greek language, an assumption that delayed decipherment for decades. Ventris's breakthrough—demonstrating that Linear B was an early form of Greek—not only revealed the language of the Mycenaean civilization but also pushed back the documented history of Greek by approximately 500 years, to the Bronze Age.

The tablets, preserved by fires that destroyed the palaces, contain primarily administrative records: inventories of agricultural products, livestock tallies, personnel lists, and religious offerings. This bureaucratic content initially disappointed scholars hoping for literary texts or historical narratives. However, these mundane records have proven invaluable for reconstructing Mycenaean society. The tablets reveal a complex palace-centered economy in which scribes meticulously tracked resources, assigned workers to specific tasks, and recorded tribute from subject territories. Religious dedications to deities later central to Classical Greek religion—Zeus, Poseidon, Athena—demonstrate continuity between Mycenaean and later Greek religious practices.

Yet Linear B's limitations as a historical source are significant. The tablets represent brief records meant to be erased and reused; they survived only by accident when fires baked them into permanent ceramic. Consequently, we have snapshots of specific moments in palace administration, but no continuous historical narrative. The script itself, adapted from the earlier Linear A writing system of Minoan Crete, fits Greek awkwardly—it cannot adequately represent certain Greek sounds and leaves grammatical features ambiguous. Decipherment requires substantial contextual inference, and many tablets remain partially or wholly incomprehensible.

Moreover, Linear B texts reflect only the perspective and interests of palace bureaucracies. We learn about the concerns of palace administrators—counting sheep, allocating rations, organizing religious festivals—but hear nothing directly from farmers, artisans, or other social groups. This creates a distorted picture of Mycenaean society, emphasizing centralized control and administrative complexity while obscuring aspects of daily life, social relationships, and cultural practices that fell outside bureaucratic purview. The tablets tell us that wool textile production was important to the palace economy, but reveal little about who wore these textiles or how they were used.

Archaeological evidence complements Linear B texts but sometimes complicates interpretation. Excavations reveal that Mycenaean civilization was characterized by fortified palace centers, extensive trade networks spanning the Mediterranean, sophisticated artistic production, and eventually catastrophic destruction around 1200 BCE. The reasons for this collapse remain controversial. Some scholars argue that the centralized palace economies documented in Linear B texts made Mycenaean society vulnerable to disruption—when palaces fell, the entire administrative and economic system collapsed. Others point to broader regional upheavals, including migrations, climate change, and the mysterious "Sea Peoples" mentioned in Egyptian sources. The Linear B tablets, with their narrow temporal and thematic scope, cannot definitively resolve these debates, but they provide crucial evidence about the economic and social organization that collapsed. Understanding this literate bureaucratic civilization's ultimate fate requires integrating textual evidence with archaeological data and comparative analysis of other contemporary Bronze Age societies.`;

