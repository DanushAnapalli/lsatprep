const passage1 = `The distinction between employees and independent contractors has become increasingly contentious as the nature of work evolves. This classification determines whether workers receive minimum wage and overtime protections, employer-provided benefits, unemployment insurance, workers' compensation, and the right to unionize—benefits that collectively constitute a significant portion of total compensation. Employers benefit from classifying workers as independent contractors rather than employees, as contractors are responsible for their own taxes and do not receive statutory protections or benefits. However, misclassification can result in substantial penalties and liability for back wages and benefits.

The traditional common-law test for employment status, which courts have applied for decades, focuses on the degree of control an employer exercises over a worker. Under this test, courts examine factors such as the employer's right to control how work is performed, whether the worker uses their own tools and equipment, how the worker is compensated, and whether the relationship is permanent or temporary. Workers whose manner and means of work are controlled by the hiring party are generally considered employees, while those who maintain autonomy in how they complete their work are considered independent contractors. This standard, developed in an era of factory employment and traditional office work, rested on the assumption that control and economic dependence were closely correlated.

However, critics argue that the control test inadequately captures economic realities in modern work arrangements. Technology enables companies to closely monitor and direct workers' activities without the traditional indicia of control. A rideshare driver, for example, may choose when to work and use their own vehicle, suggesting independent contractor status under the control test. Yet the company's algorithm determines which rides they receive, sets prices, evaluates their performance through customer ratings, and can terminate their access to the platform based on these ratings. The driver's economic relationship with the company may resemble traditional employment more than genuine entrepreneurial independence, yet the control test may not capture this reality.

Several jurisdictions have adopted alternative tests that focus more explicitly on economic dependence. The "ABC test," used in California and several other states, presumes workers are employees unless the hiring entity demonstrates that: (A) the worker is free from control and direction; (B) the work performed is outside the usual course of the hiring entity's business; and (C) the worker is customarily engaged in an independently established trade or business. This test is more difficult for employers to satisfy, particularly the requirement that work fall outside the hiring entity's usual business—a rideshare driver provides transportation services for a transportation company, making independent contractor classification challenging under this standard.

The policy implications of these different tests are substantial. Broader definitions of employment would extend protections to more workers but might reduce flexibility that benefits both workers seeking autonomy and companies building scalable business models. Some economists argue that mandatory employee classification would reduce overall employment, as companies would hire fewer workers if each came with significant additional costs. Others counter that independent contractor status often represents a false choice—workers accept it due to lack of alternatives, not preference for flexibility—and that proper classification would not significantly reduce employment while substantially improving working conditions. The debate reflects deeper questions about how labor law should adapt to technological change and whether traditional employment protections remain the appropriate framework for regulating work in the twenty-first century.`;

// PASSAGE 2: At-Will Employment and Wrongful Discharge
const passage2 = `The doctrine of at-will employment, which permits employers to terminate employees at any time for any reason not prohibited by law, represents the default rule in American labor law. Unlike many other developed nations, which require employers to demonstrate just cause for termination, the United States generally allows employers to dismiss workers for good reasons, bad reasons, or no reason at all, as long as the reason is not illegal. This doctrine, which emerged in the late nineteenth century, reflected classical liberal economic theory emphasizing freedom of contract and minimal government interference in private employment relationships.

The at-will doctrine has faced increasing limitations through statutory and common-law exceptions that create "wrongful discharge" claims when terminations violate public policy. Federal and state statutes prohibit termination based on protected characteristics such as race, gender, age, and disability, and retaliation for exercising statutory rights such as filing workers' compensation claims or reporting safety violations. These statutory exceptions represent explicit legislative judgments that freedom of contract must yield to competing social values, including anti-discrimination principles and the need to ensure workers can assert their legal rights without fear of retaliation.

Common-law exceptions to at-will employment, developed through judicial decisions, extend protection to additional categories of terminations deemed contrary to public policy. Courts in most states recognize a public policy exception that prohibits termination for reasons that violate clearly established public policy, such as firing an employee for refusing to commit perjury or for serving on a jury. Some jurisdictions recognize an implied covenant of good faith and fair dealing in employment contracts, preventing employers from terminating employees in bad faith or for malicious reasons. A smaller number of states recognize that employee handbooks or oral representations can create implied contracts limiting the employer's right to terminate at will.

However, these exceptions remain relatively narrow, and courts generally interpret them restrictively to preserve the at-will doctrine's fundamental premise. Employers retain broad discretion to terminate workers for reasons unrelated to illegal discrimination or public policy violations, including poor business decisions, personality conflicts, or simple preference for other candidates. This asymmetry—employees typically must provide notice and cannot simply abandon their positions, while employers can terminate immediately for any lawful reason—reflects the doctrine's origins in an era of stark inequality in bargaining power between employers and workers.

Critics of at-will employment argue that it creates perverse incentives and unjust outcomes. Workers may hesitate to report safety violations, workplace harassment, or other problems if they fear termination, even when statutes nominally protect such reporting. The doctrine's uncertainty—workers often cannot predict whether their terminations will be deemed lawful—creates anxiety and inhibits worker voice. Moreover, the cost and difficulty of litigation means that many workers with meritorious wrongful discharge claims cannot vindicate their rights, effectively negating statutory protections. These critics advocate for a just-cause standard that would require employers to demonstrate legitimate business reasons for terminations, shifting the burden of proof and providing workers with greater job security. Defenders of at-will employment counter that it provides necessary flexibility for employers to respond to changing business conditions and that greater restrictions on termination would reduce hiring, harm productivity, and ultimately reduce overall employment opportunities.`;

// PASSAGE 3: Collective Bargaining and Union Representation
const passage3 = `The National Labor Relations Act (NLRA), enacted in 1935, guarantees private-sector workers the right to organize unions and engage in collective bargaining with employers. The Act's framers viewed collective bargaining as essential for addressing the inequality of bargaining power between individual workers and employers, reasoning that workers acting collectively could negotiate more effectively for wages, benefits, and working conditions. The NLRA established a framework in which workers could vote to unionize their workplace, and employers would then be required to negotiate in good faith with the union as the exclusive representative of all bargaining unit employees.

However, union membership in the private sector has declined dramatically from approximately 35 percent of workers in the 1950s to less than 7 percent today. Multiple factors explain this decline. Economic shifts toward service industries and away from manufacturing—traditionally the most heavily unionized sectors—account for part of the decline. Geographic shifts toward states with "right-to-work" laws, which prohibit requiring union membership or fee payment as a condition of employment, have also contributed. Additionally, many scholars point to aggressive employer opposition to unionization, including tactics such as mandatory anti-union meetings, threats of plant closure, and termination of union supporters that, while illegal, are difficult to prevent through the NLRA's enforcement mechanisms.

The decline of collective bargaining has coincided with growing wage inequality and stagnant wage growth for many workers. Some economists argue this correlation reflects causation: unions' ability to negotiate higher wages and reduce wage dispersion between high- and low-skilled workers within firms contributed to the more compressed wage distribution of the mid-twentieth century. As union density declined, this equalizing effect disappeared, contributing to rising inequality. These economists point to research showing that union workers earn approximately 10-20 percent more than comparable non-union workers and receive substantially better benefits, suggesting that the decline of collective bargaining has directly reduced worker compensation.

However, other economists question whether revitalizing collective bargaining represents an effective policy response to contemporary labor market challenges. They note that unions may secure higher wages for their members partly by reducing employment, as employers substitute capital for now-more-expensive labor. Furthermore, the adversarial nature of traditional collective bargaining, with its emphasis on detailed work rules and seniority systems, may reduce the flexibility that firms need to respond to rapid technological change. These economists argue that alternative mechanisms, such as works councils that promote labor-management cooperation or individual employment contracts with portable benefits, might better address worker concerns while preserving necessary flexibility.

A third perspective suggests that the NLRA framework itself requires reform to remain relevant. The Act's exclusive representation model assumes relatively stable employment relationships in industrial workplaces, but contemporary workers often change jobs frequently and work in smaller, geographically dispersed workplaces where traditional organizing is difficult. Some labor law scholars propose reforms such as sectoral bargaining—in which unions negotiate with all employers in an industry rather than workplace-by-workplace—or minority unionism—allowing unions to represent interested workers without achieving majority support. These reforms, modeled on systems used in European countries, might make collective representation more accessible while adapting to contemporary employment patterns. However, implementing such fundamental changes would require legislative action that appears unlikely given deep political divisions over labor policy.`;

