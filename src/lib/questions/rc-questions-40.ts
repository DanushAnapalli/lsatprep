// PASSAGE 1: Consciousness and the Hard Problem
const passage1 = `The "hard problem of consciousness," a term coined by philosopher David Chalmers, refers to the challenge of explaining why and how physical processes in the brain give rise to subjective experience—the felt quality of seeing red, tasting chocolate, or feeling pain. While neuroscience has made remarkable progress in identifying neural correlates of consciousness—the brain states associated with particular conscious experiences—this correlational knowledge does not explain why these neural processes should produce subjective experience at all. We can, in principle, imagine beings that process information identically to conscious humans but lack any inner experience, what philosophers call "philosophical zombies." The conceivability of such zombies suggests that consciousness involves something beyond mere information processing.

Materialist philosophers argue that the hard problem reflects conceptual confusion rather than a genuine mystery. They contend that once we fully understand the functional organization of the brain—how it processes information, integrates sensory inputs, controls behavior, and represents its own states—we will have explained consciousness. The apparent "extra" quality of subjective experience, they argue, is nothing over and above the physical processes; it is simply how these processes appear from the internal perspective of the system. The feeling that consciousness transcends physical explanation results from our limited current understanding of complex neural processes, not from consciousness actually transcending the physical.

However, critics respond that this materialist position simply denies the phenomenon it purports to explain. The subjective, qualitative character of experience—what philosophers call "qualia"—seems fundamentally different from any amount of information about physical processes. Knowing every neural detail about how the brain processes visual information does not tell us what it is like to experience the redness of red. This explanatory gap, critics argue, cannot be closed by more detailed neuroscience, because first-person subjective experience and third-person objective description occupy different explanatory domains that cannot be reduced to each other.

Some philosophers propose that consciousness might be a fundamental feature of reality, not reducible to or emergent from physical processes. This view, called "panpsychism," suggests that consciousness or proto-consciousness exists at all levels of physical reality, and that complex conscious experiences arise from the combination of simpler conscious elements. While this may seem outlandish, proponents argue that it avoids the seemingly intractable problem of explaining how consciousness could arise from wholly non-conscious matter. If some form of consciousness is ubiquitous, the hard problem becomes not how consciousness arises from the non-conscious, but how simple conscious elements combine to create complex experiences.

Other theorists pursue "integrated information theory," which proposes a mathematical framework for quantifying consciousness based on the degree of integrated information in a system. According to this theory, consciousness corresponds to the system's capacity to integrate information—its ability to be in one state rather than many possible alternatives. This approach attempts to bridge the explanatory gap by providing a rigorous measure of consciousness that applies to any physical system, potentially explaining not only human consciousness but also degrees of consciousness in animals, artificial intelligence, and perhaps even simple physical systems. Critics question whether mathematical measures of information integration truly capture the subjective quality of experience, or whether they merely quantify certain functional properties while leaving the hard problem unresolved.`;

// PASSAGE 2: Free Will and Determinism
const passage2 = `The debate over free will centers on whether humans possess genuine agency—the capacity to choose among alternative possibilities—or whether our choices are predetermined by prior causes operating according to natural laws. Determinism holds that given the state of the universe at any moment and the laws of nature, all future states are fixed. If determinism is true, and if our choices are part of the natural causal order, then our choices are the inevitable consequences of prior events stretching back before our birth. This seems to conflict with our phenomenological sense that we freely choose our actions and could have done otherwise.

Libertarian free will theorists (not to be confused with political libertarianism) argue that genuine free will requires indeterminism—our choices must not be wholly determined by prior causes. Some appeal to quantum mechanics, which describes certain events as genuinely random rather than determined by prior states, suggesting that human choice might involve similar indeterminacy. However, critics respond that randomness is not freedom; a choice caused by random quantum events seems no more "free" than a choice wholly determined by prior causes. Freedom, they argue, requires not just indeterminism but also appropriate causal connection between our reasons, values, and decisions.

Compatibilists offer a third position, arguing that free will is compatible with determinism. They distinguish between choices caused by internal factors (desires, beliefs, values) and those caused by external coercion or compulsion. A choice is free, compatibilists claim, when it flows from the agent's own desires and deliberation, even if those desires are themselves determined by prior causes. A person who deliberates about career options and chooses based on their values exercises free will, while a person forced at gunpoint to hand over money does not, regardless of whether the underlying neural processes are deterministic. What matters for freedom is the causal chain, not whether the chain itself could have been different.

However, hard determinists and hard incompatibilists argue that compatibilism merely redefines free will to avoid the problem rather than solving it. If all our desires, values, and deliberative processes are themselves the determined consequences of factors beyond our control—genetics, upbringing, neural chemistry—then the distinction between internal and external causes becomes superficial. We are, in this view, sophisticated machines whose outputs (choices) are fully determined by inputs (prior causes), and the fact that some inputs pass through complex deliberative processes before producing outputs does not make those outputs genuinely "free" in any metaphysically significant sense.

The practical implications of this debate are profound. Our legal system distinguishes between voluntary and involuntary actions, assigns criminal responsibility, and imposes punishment partly on grounds of desert—the notion that wrongdoers deserve punishment for their freely chosen actions. If hard determinism is correct, some philosophers argue, these practices rest on a metaphysical error. We might still have consequentialist reasons to maintain legal sanctions (deterrence, incapacitation), but retributive punishment—punishment justified by moral desert—would be inappropriate. Compatibilists respond that their analysis preserves the meaningful distinctions necessary for moral and legal responsibility without requiring metaphysically problematic libertarian free will. The debate continues to shape discussions in philosophy, neuroscience, and law about the nature of human agency and responsibility.`;

// PASSAGE 3: Personal Identity and the Self
const passage3 = `What makes you the same person you were ten years ago, despite the dramatic physical and psychological changes you have undergone? The problem of personal identity asks what constitutes the persistence of a person over time—what criterion determines that the person who exists at one time is identical to a person who exists at another time. This question has significant practical implications for issues ranging from moral responsibility (can you be held responsible for actions performed by your past self?) to advance medical directives (should doctors honor the wishes expressed by a patient's earlier self when treating their later, demented self?).

The bodily criterion of personal identity holds that persons persist through continuity of their physical bodies. You are the same person you were as a child because you inhabit the same continuously existing biological organism. This criterion aligns with our ordinary practices of reidentifying people by their physical appearance and provides a clear, objective standard. However, thought experiments challenge this criterion's adequacy. If your brain were transplanted into another body, intuition suggests that you would "go with your brain" rather than remaining with your original body. This suggests that psychological continuity, not mere bodily continuity, grounds personal identity.

Psychological continuity theories hold that personal identity consists in continuity of mental states—memories, personality traits, beliefs, and values. You are the same person as your childhood self because you remember their experiences, inherited their personality, and continue their projects. John Locke's influential version of this view emphasized memory: a person at one time is identical to a person at an earlier time if the later person remembers the experiences of the earlier person. However, critics point out that memory is neither necessary nor sufficient for personal identity. You remain the same person even when you forget experiences, and false memories do not make you identical to someone whose experiences you mistakenly "remember."

Contemporary psychological continuity theories address these concerns by appealing to broader psychological connections and continuity rather than memory alone. Derek Parfit's version emphasizes overlapping chains of psychological connectedness: while you might not remember your earliest childhood experiences, you remember later childhood experiences that themselves involved memories of earlier ones, creating a continuous chain. Parfit goes further, arguing that this analysis reveals personal identity to be less important than we ordinarily assume. What matters for rational concern and moral responsibility is psychological continuity and connectedness, which come in degrees, not the all-or-nothing metaphysical fact of identity.

However, some philosophers argue that no criterion of continuity or connection can fully capture personal identity, because identity is not analyzable in terms of other relations. On this view, persons are irreducible entities—selves or souls—that persist through time not by virtue of any continuity of physical or psychological properties, but simply by being the same substantial entity. This position faces the challenge of explaining how we could ever know about such entities or reidentify them over time if they are not constituted by any observable physical or psychological features. The debate continues, with implications not only for theoretical metaphysics but also for practical questions about medical ethics, criminal justice, and our understanding of human life and values.`;

